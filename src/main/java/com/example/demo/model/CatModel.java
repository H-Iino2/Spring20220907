package com.example.demo.model;

public class CatModel {
	private String call;
	private String name;
	private int age;

	public String getCall() {
		return this.call;
	}
	public void setCall(String call) {
		this.call = call;
	}
	
	public String getName(String name) {
		return this.name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}

